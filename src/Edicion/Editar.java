    private void jtnombreKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jtnombreKeyPressed
        if (evt.getKeyCode() == 27) { //27 es el código de la tecla esc //cerra esta ventana        //devuelve 0 = si, 1 = no
            int w = JOptionPane.showConfirmDialog(null, "¿Desea salir sin guardar?",
                    "Confirmar salida", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
            if (w == 0) {//cierra ventana sin guardar
                this.dispose();
            }
        }
    }//GEN-LAST:event_jtnombreKeyPressed

