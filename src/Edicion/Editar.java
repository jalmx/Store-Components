    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed

        String edo;
        Principal p = new Principal();

        Vector registro = new Vector();

        registro.add(id);//ID se lo paso vacio por que la base de dato lo pone automaticamente y por que si no lo paso me marca error el vector
        registro.add(jtnombre.getText());//Nombre
        registro.add(jttipo.getText());//Tipo
        registro.add(jtcodigo.getText());//Codigo
        registro.add(jtdescripcion.getText());//Descripcion
        registro.add(jtencapsulado.getText());//Encapsulado
//        registro.add(pathPDF);//path del archivo, es decir, donde esta guardado
        registro.add(Variables.archivo);//path del archivo
        registro.add(jtubicacion.getText());//Ubicacion

        if (jcestado.getSelectedIndex() == 0) {
            edo = "Disponible";
        } else {
            edo = "No Disponible";
        }
        registro.add(edo);//Estado
        registro.add(Double.parseDouble(jtcantidad.getText()));//cantidad de elementos en bodega

        if (insertar == true) {

            boolean modificado = Variables.BD.actualizarDatos(registro);

            if (modificado) {
//                Variables.archivo = "Sin archivo";
                JOptionPane.showMessageDialog(this, "Registro actualizado");
                this.dispose();//cierra la ventana de edicion con el registro actualizado
            } else {
                JOptionPane.showMessageDialog(null, "Error al actualizar");
            }
        }
        this.limpiar();
    }//GEN-LAST:event_jButton2ActionPerformed

