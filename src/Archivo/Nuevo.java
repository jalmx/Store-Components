    private void jtnombreKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jtnombreKeyPressed
        if (evt.getKeyCode() == 27) //27 es el código de la tecla esc //cerra esta ventana
        {
            if (jtnombre.getText().isEmpty()) {
                this.dispose();
            } else {
                this.limpiar();
            }
        }

    }//GEN-LAST:event_jtnombreKeyPressed

    private void jcestadoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jcestadoActionPerformed
        if (jcestado.getSelectedIndex() == 1) {
            jtcantidad.setEnabled(false);
        }
        if (jcestado.getSelectedIndex() == 0) {
            jtcantidad.setEnabled(true);
        }
    }//GEN-LAST:event_jcestadoActionPerformed

    private void emtySpace() {
        if (jtnombre.getText().isEmpty()) {
            jtnombre.setText("-");
        }

        if (jttipo.getText().isEmpty()) {
            jttipo.setText("-");
        }

        if (jtencapsulado.getText().isEmpty()) {
            jtencapsulado.setText("-");
        }

        if (jtubicacion.getText().isEmpty()) {
            jtubicacion.setText("-");
        }

        if (jtdescripcion.getText().isEmpty()) {
            jtdescripcion.setText("-");
        }

        if (jcestado.getSelectedIndex() == 1) {
            jtcantidad.setText("0");
        }

        if (jcestado.getSelectedIndex() == 0) {
            jtcantidad.getText().isEmpty();
        }

    }

    public void limpiar() {
        jtnombre.setText("");
        jttipo.setText("");
        jtcodigo.setText("");
        jtdescripcion.setText("");
        jtencapsulado.setText("");
        jtubicacion.setText("");
        jcestado.setSelectedIndex(0);//que ponga disponible
        jtcantidad.setText("");

    }

    private int enumeracion() {
        Vector result = BaseDeDatos.getDB().getDatos();

        int x = result.size();
        return x + 1;
    }

