    private void jbAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbAceptarActionPerformed
        this.emtySpace();

        pathPDF = Variables.archivo;
        String edo;
        Principal p = new Principal();

        Vector registro = new Vector();

        registro.add("");//ID se lo paso vacio por que la base de dato lo pone automaticamente y por que si no lo paso me marca error el vector
        registro.add(jtnombre.getText());//Nombre
        registro.add(jttipo.getText());//Tipo
        registro.add(jtcodigo.getText());//Codigo
        registro.add(jtdescripcion.getText());//Descripcion
        registro.add(jtencapsulado.getText());//Encapsulado
        registro.add(pathPDF);//path del archivo, es decir, donde esta guardado
        registro.add(jtubicacion.getText());//Ubicacion

        if (jcestado.getSelectedIndex() == 0) {
            edo = "Disponible";
        } else {
            edo = "No Disponible";
            jtcantidad.setText("0");
        }
        registro.add(edo);//Estado

        try {
            registro.add(Double.parseDouble(jtcantidad.getText()));//cantidad de elementos en bodega
            if (insertar == true) {
            boolean insertado = BaseDeDatos.getDB().insertarDato(registro); //Retorna valor booleano, true que si fue insertado y falso de lo contrario

            if (insertado) {
                Variables.archivo = "Sin archivo";//esto es para que si agrego otro dispo y no agrego DS no agrege el DS anterior
                p.actualizarTabla();
                JOptionPane.showMessageDialog(this, "Registro guardado");
                this.limpiar();
                this.dispose();
            } else {
                JOptionPane.showMessageDialog(null, "Error al insertar");
                this.limpiar();
            }
        }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "La cantidad es incorrecta");
        }
    }//GEN-LAST:event_jbAceptarActionPerformed

